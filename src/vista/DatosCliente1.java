    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (cliente.getNif().equals(" ") || cliente.getNombre().equals("") || cliente.getApellidos().equals("") ||
                cliente.getDireccion().equals("") || cliente.getPoblacion().equals("")) {
            JOptionPane.showMessageDialog(this, "No puede haber ningun campo en blanco.");
        }else if (jComboBox1.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar una poblaci贸n.");
        } else if (cliente.getNif().length() != 9) {
            JOptionPane.showMessageDialog(this, "NIF de longitud incorrecta.");
        }  else if (clientes.existeCliente(cliente)) {
            JOptionPane.showMessageDialog(this, "Ya existe un cliente con este NIF");
        } else {
            clientes.altaCliente(cliente);
            ficheroClientes.grabar(clientes);
            JOptionPane.showMessageDialog(this, "Cliente dado de alta.");
            dispose();
        }
            
    }//GEN-LAST:event_jButton2ActionPerformed

   

